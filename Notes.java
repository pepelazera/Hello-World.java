package Entities;

public class Notes {

    private int n;
    private double bigger;
    private double smaller;

    public int getN() {
        return n;
    }
    public void setN(int n) {
        this.n = n;
    }

    public double getBigger() {
        return bigger;
    }
    public void setBigger(double bigger) {
        this.bigger = bigger;
    }

    public double getSmaller() {
        return smaller;
    }
    public void setSmaller(double smaller) {
        this.smaller = smaller;
    }
}
